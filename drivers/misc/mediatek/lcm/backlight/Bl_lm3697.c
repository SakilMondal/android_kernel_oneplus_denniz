int lm3697_setbacklight(unsigned int level)
{
	unsigned int MSB = 0;
	unsigned int LSB = 0;

	lcd_bl_en_setting(1);

	if (level >=2047)
		level = 2047;

	/*
	* add for backlight IC KTD3136
	*/
	if (is_lm3697 == 2) {   /*KTD3136*/
		if (KTD3136_EXPONENTIAL) {
			MSB = (backlight_ktd3136_buf[level] >> 3) & 0xFF;
			LSB = backlight_ktd3136_buf[level] & 0x07;
		} else {
			MSB = (level >> 3) & 0xFF;
			LSB = level & 0x07;
		}

		if (level == 0) {
			lm3697_write_byte(0x04, LSB);
			lm3697_write_byte(0x05, MSB);
		} else {
			lm3697_write_byte(0x04, LSB);
			lm3697_write_byte(0x05, MSB);
			lm3697_write_byte(0x02, 0x99);
			/* esd_recovery_backlight_level = level; */
		}
	} else if (is_lm3697 == 1) {  /*LM3697*/
		if (LM3697_EXPONENTIAL) {
			MSB = (backlight_buf[level] >> 3) & 0xFF;
			LSB = backlight_buf[level] & 0x07;
		} else {
			MSB = (level >> 3) & 0xFF;
			LSB = level & 0x07;
		}

		if (level == 0) {
			lm3697_write_byte(0x20, LSB);
			lm3697_write_byte(0x21, MSB);
		} else {
			lm3697_write_byte(0x24, 0x01);
			lm3697_write_byte(0x20, LSB);
			lm3697_write_byte(0x21, MSB);
			/* esd_recovery_backlight_level = level; */
		}
	} else {
		if (MP3188_EXPONENTIAL) {
			MSB = (backlight_mp3188_buf[level] >> 3) & 0xFF;
			LSB = backlight_mp3188_buf[level] & 0x07;
		} else {
			MSB = (level >> 3) & 0xFF;
			LSB = level & 0x07;
		}
		if (level == 0) {
			lm3697_write_byte(0x18, LSB);
			lm3697_write_byte(0x19, MSB);
		} else {
			lm3697_write_byte(0x18, LSB);
			lm3697_write_byte(0x19, MSB);
			lm3697_write_byte(0x10, 0x0d);
			/* esd_recovery_backlight_level = level; */
		}
	}
	printk("<zrc>%s level is %d\n", __func__, level);
	return level;
}

