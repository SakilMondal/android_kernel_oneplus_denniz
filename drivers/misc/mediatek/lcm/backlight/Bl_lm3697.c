int lm3697_write_byte(unsigned char addr,  unsigned char value)
{
	int ret = 0;
	struct i2c_client *client = i2c_lm3697;
	unsigned char write_data[2] = {0};
	write_data[0] = addr;
	write_data[1] = value;
	if (client != NULL) {
		ret=i2c_master_send(client, write_data, 2);
	}
	if (ret<0) {
		printk("Bl lm3697 Write I2C fail !!\n");
	}
	return ret ;
}
EXPORT_SYMBOL(lm3697_write_byte);

int lm3697_read_byte(unsigned char addr, char  *value, int length)
{
	int ret = 0;
	struct i2c_client *client = i2c_lm3697;
	struct i2c_msg msg[2] = {
		{
			.addr = client->addr,
			.buf = &addr,
			.len = 1,
		},
		{
			.addr = client->addr,
			.flags = I2C_M_RD,
			.buf = value,
			.len = length,
		},
	};
	ret = i2c_transfer(client->adapter, msg, 2);
	if (ret != 2) {
		printk("Bl lm3697 Read I2C fail !!ret = %d\n",ret);
		ret = -EIO;
	} else {
		ret = 0;
	}
	return ret ;
}
EXPORT_SYMBOL(lm3697_read_byte);

